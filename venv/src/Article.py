class Article:
    author
    content
    vector


    def setVector(self,vector):
        self.vector = vector


    def setAuthor(self,author):
        self.author = author


    def setContent(self,content):
        self.content = content


    def getVector(self):
        return self.vector

    def getAuthor(self):
        return self.author

    def getContent(self):
        return self.content